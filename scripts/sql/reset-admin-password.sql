  قالب SQL — إعادة تعيين كلمة مرور المستخدم admin
  =============================================================================

  تحذير:
  - هذا الملف قالب توثيقي وليس مخصصًا للتشغيل العشوائي في الإنتاج.
  - لا يحذف أي بيانات ولا يمس جداول أخرى غير Users (عند التفعيل اليدوي).
  - Uqeb يخزّن كلمات المرور كـ BCrypt hash (انظر AuthService / DbSeeder).

  لماذا لا يُحدَّث الـ hash مباشرة من SQL؟
  - SQL Server لا يولّد BCrypt بشكل قياسي داخل T-SQL.
  - إدخال hash خاطئ يمنع تسجيل الدخول دون إمكانية استرجاع سهل.

  الطرق الموصى بها لإعادة التعيين:
  1) من واجهة النظام: المستخدمون (Admin) → تعديل المستخدم admin → كلمة مرور جديدة.
  2) من جهاز بناء: توليد hash عبر التطبيق ثم تحديث يدوي بعد اختبار في بيئة غير إنتاجية.
  3) endpoint/أداة إدارية مستقبلية مخصصة لإعادة التعيين (إن أُضيفت لاحقًا).

  =============================================================================
*/

USE [UqebDb];
GO

-- عرض الحالة الحالية (قراءة فقط)
SELECT
    Id,
    Username,
    FullName,
  IsActive,
    LEFT(PasswordHash, 20) + N'...' AS PasswordHashPreview
FROM dbo.Users
WHERE Username = N'admin';
GO

